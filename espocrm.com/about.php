                        <h2>In brief</h2>
                        <p>Since 2011, our company helps businesses to develop faster and succeed. In 2014, we released <strong><?=$namehost?></strong>, a platform that solves simple and tough challenges a company faces at each stage of its lifecycle, from basic customer relationship management to complex business process automation. Now, <?=$namehost?> is a technology that is focused on putting customers at the heart of your business and accelerating your company’s growth.</p>
                        <h2>In more detail</h2>
                        <p><strong>Letrium LTD</strong> was founded in Ukraine in 2011 by a group of three friends Oleksii Avramenko, Yuri Kuznetsov, and Taras Machyshyn. During the first years of its existence, the company was dealing with CRM-related customization and development services. Most of the clients &#8211; small and mid-size companies &#8211; were looking for a customized solution that can address their industry-specific needs. The out-of-the-box CRMs were either too expensive for a small company or too difficult to self-customize to fit the requirements of a particular business.</p>
