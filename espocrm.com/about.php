                        <p>In 2014, during a regular weekly meeting, Oleksii Avramenko initiated an in-depth discussion of the future of the CRM market and open-source solutions. This lengthy conversation has sparked the idea to create open-source software that would be affordable for a company of any size and at the same time easily tailored to the needs of any industry.</p>
                        <p>Having analyzed the demands of the market, the team divided the project into parts and began working on turning the idea into reality. Yuri Kuznetsov, the chief developer of the project, developed the architecture of the application, the front-end framewok, UI/UX. Taras Machyshyn worked on parts of the back-end, installation, upgrade scripts, database schema generation, application metadata. Oleksii Avramenko dealt with website creation, database administration, sales, and marketing.</p>
                        <p>Finally, the first version of <?=$namehost?> was released after about 10 months of hard work on the project. The product was published on Sourceforge and soon became a success. It was a groundbreaking solution with a simple, intuitive interface that offered tools for increasing business performance for free. We received positive feedback from the first users. They recommended the product to their friends, left reviews on various platforms, and so the number of clients was growing.</p>
                        <p>Nowadays, <?=$namehost?> is utilized by more than 50,000 companies all around the globe and the number continues to grow. Backed up by the enthusiastic team of developers, the software is improving exponentially with new features, bug fixes, and updates being released regularly. The pride in helping businesses to succeed inspires us to perfect our solution and provide even better service to our customers.</p>
